# Crie um conjunto vazio chamado frutas e adicione as
# seguintes frutas a ele: "maçã", "banana", "uva", "laranja"
# e "morango". Em seguida, imprima o conjunto.

# Criando um conjunto vazio chamado frutas
frutas = set()

# Adicionando as frutas ao conjunto
frutas.add("maçã")
frutas.add("banana")
frutas.add("uva")
frutas.add("laranja")
frutas.add("morango")

# Imprimindo o conjunto de frutas
print(frutas)